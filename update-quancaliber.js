// ============================================================
//  主逻辑
// ============================================================
const ROOT = process.argv[2] || __dirname;
const PROVINCES_DIR = path.join(ROOT, 'provinces');

let updated = 0;
let skipped = 0;
let errors = [];

for (const [yearStr, provinceData] of Object.entries(QUANCALIBER)) {
  const year = parseInt(yearStr);
  for (const [provName, value] of Object.entries(provinceData)) {
    if (!value) continue;  // 跳过null（待补数据）

    const key = NAME_TO_KEY[provName];
    if (!key) {
      errors.push(`未知省份：${provName}`);
      continue;
    }

    const jsonPath = path.join(PROVINCES_DIR, `${key}.json`);
    if (!fs.existsSync(jsonPath)) {
      errors.push(`文件不存在：${key}.json`);
      continue;
    }

    try {
      const raw = fs.readFileSync(jsonPath, 'utf8');
      const prov = JSON.parse(raw);

      if (!prov.avg_salary_history) prov.avg_salary_history = {};

      const oldVal = prov.avg_salary_history[year];
      prov.avg_salary_history[year] = value;

      // 更新 data_source 备注
      if (!prov.notes) prov.notes = {};
      if (!prov.notes.data_source) prov.notes.data_source = {};
      prov.notes.data_source.quancaliber = `全口径数据已更新至${year}年。来源：m12333.cn 汇总表。`;

      fs.writeFileSync(jsonPath, JSON.stringify(prov, null, 2) + '\n', 'utf8');
      updated++;
      if (oldVal && oldVal !== value) {
        console.log(`  ✏️  ${provName}(${year}): ${oldVal} → ${value}`);
      }
    } catch (e) {
      errors.push(`写入失败 ${key}.json: ${e.message}`);
    }
  }
}

console.log(`\n✅ 更新完成：${updated} 条`);
if (errors.length) {
  console.log(`\n⚠️  错误（${errors.length}）：`);
  errors.forEach(e => console.log(`  - ${e}`));
}

// 输出待补数据的省份清单
console.log('\n📋 待补数据（全口径，元/年）：');
for (const year of [2020, 2021, 2022, 2023, 2024]) {
  const missing = [];
  for (const [provName, value] of Object.entries(QUANCALIBER[year])) {
    if (!value) missing.push(provName);
  }
  if (missing.length) {
    console.log(`  ${year}年：待补 ${missing.join('、')}`);
  }
}
