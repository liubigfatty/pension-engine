let updated = 0, skipped = 0;
const errors = [];
const updatedProvs = new Set();

for (const [yearStr, provData] of Object.entries(QUANCALIBER)) {
  const year = parseInt(yearStr);
  for (const [provName, value] of Object.entries(provData)) {
    if (!value) { skipped++; continue; }
    const key = NAME_TO_KEY[provName];
    if (!key) { errors.push(`未知省份：${provName}`); continue; }
    const fpath = path.join(PROVINCES_DIR, `${key}.json`);
    if (!fs.existsSync(fpath)) { errors.push(`文件不存在：${key}.json`); continue; }
    try {
      const prov = JSON.parse(fs.readFileSync(fpath, 'utf8'));
      if (!prov.avg_salary_history) prov.avg_salary_history = {};
      const oldVal = prov.avg_salary_history[year];
      prov.avg_salary_history[year] = value;
      // 备注
      if (!prov.notes) prov.notes = {};
      if (!prov.notes.data_source) prov.notes.data_source = {};
      prov.notes.data_source.quancaliber = `全口径数据已更新至${year}年（m12333.cn汇总表）。`;
      fs.writeFileSync(fpath, JSON.stringify(prov, null, 2) + '\n', 'utf8');
      updated++;
      updatedProvs.add(key);
      if (oldVal && Math.abs(oldVal - value) > 1) {
        console.log(`  ✏️  ${provName}(${year}): ${oldVal} → ${value}`);
      }
    } catch (e) {
      errors.push(`写入失败 ${key}.json: ${e.message}`);
    }
  }
}

// 同步到js/docs/cloudfunctions副本
const COPIES = [
  'js/provinces', 'docs/js/provinces', 'cloudfunctions/calculate/provinces',
];
for (const prov of updatedProvs) {
  const src = path.join(PROVINCES_DIR, `${prov}.json`);
  for (const dir of COPIES) {
    const dst = path.join(ROOT, dir, `${prov}.json`);
    try { fs.copyFileSync(src, dst); } catch(e) {}
  }
  // pension-engine 需手动复制（沙箱保护）
}

console.log(`\n✅ 更新完成：${updated} 条，跳过（null）：${skipped} 条`);
