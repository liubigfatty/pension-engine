def estimate_base_rates(real_data, coeff):
    base = {}
    if real_data:
        for y, v in real_data.items():
            base[y] = round(v, 2)
    # 用山东 × 系数 填补缺失年份
    for y in range(1993, 2027):
        sy = str(y)
        if sy in base:
            continue
        ref = SHANDONG_BASE.get(sy)
        if ref:
            base[sy] = round(ref * coeff, 2)
        else:
            prev = base.get(str(y-1))
            if prev:
                base[sy] = round(prev * 1.05, 2)
    return base

